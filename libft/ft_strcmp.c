int	ft_strcmp(char *s1, char *s2)
{
	unsigned int	i;
	if (s1 == s2)
		return (0);
	if ((s1 == 0) ^ (s2 == 0))
		return (1);
	i = 0;
	while (s1[i] == s2[i] && s1[i])
		i++;
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
